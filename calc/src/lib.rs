// build calculator functions

//add two numbers
pub fn add(a: i32, b: i32) -> i32 {
    a + b
}

//substract two numbers
pub fn sub(a: i32, b: i32) -> i32 {
    a - b
}

//multiply two numbers
pub fn mult(a: i32, b: i32) -> i32 {
    a * b
}

//divide two numbers
pub fn div(a: i32, b: i32) -> i32 {
    a / b
}
